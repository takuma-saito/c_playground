#define SIZE 4096

int main() {
  void* addr = (void *)calloc(SIZE, sizeof(char));
  void (*func)();
  memcpy(addr, PAYLOAD, sizeof(PAYLOAD));
  int ret = mprotect(addr, SIZE, PROT_READ | PROT_WRITE | PROT_EXEC);
  if (ret != 0) {
    printf("errno: %d, ret: %d\n", errno, ret);
    exit(ret);
  }
  *&func = addr;
  func();
}
